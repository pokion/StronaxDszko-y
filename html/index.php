ony przez Kamila Sołowieja, ucznia klasy III Technikum informatycznego. Na koniec Dnia Otwartego na bieżni wokół boiska szkolnego można było obejrzeć przejazdy gokartów przygotowywanych do tego przez zainteresowanych uczniów naszej szkoły.

