      1959 dokonano naboru uczniów do dwóch klas pierwszych i
      zorganizowano dwie równoległe klasy drugie. Młodzież otrzymała
      zakwaterowanie w internacie Liceum Ogólnokształcącego w
      Sokółce. W roku 1960 Szkoła Rzemiosł Budowlanych przeniosła się
