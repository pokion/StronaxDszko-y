        Eliza Orzeszkowa z Pawłowskich - pisarka i publicystka; początkowo związana ze środowiskiem ziemiańskim; brała udział w akcjach powstańczych w 1863 roku. W roku 1870 osiadła w Grodnie, prowadząc żywą akcję oświatowo-pedagogiczną, m.in. w roku 1879 założyła księgarnię wydawniczą dla popularyzowania myśli pozytywistycznej (w 1882 r. zamknięta przez władze). We wczesnej twórczości czołowa przedstawicielka pozytywistycznej literatury tendencyjnej, w licznych powieściach poddała krytyce głównie bezproduktywne życie i obyczaje ziemiaństwa, którym przeciwstawiła mieszczańskie ideały moralne i cywilizacyjne ("Pamiętnik Wacławy" 1871, "Pan Graba" 1872, "Maria" 1873, "Pompalińscy" 1876); z krytyką feudalnych anachronizmów związała ostry protest przeciwko społeczno-obyczajowemu ekonomicznemu upośledzeniu kobiety("Marta" 1873) i postulat wyrównania krzywd i asymilacji Żydów ("Meir Ezofowicz" 1878). Rozczarowanie do haseł pozytywistycznych wyraziło się we wzmożonym krytycyzmie społecznym (opowiadania "Z różnych sfer", t. 1-3 1879-82), w obrazie nędzy i wyzysku ludności wiejskiej ("Niziny" 1883), ciemnoty i zabobonu na wsi, prowadzących do zbrodni ("Dziurdziowie" 1885), w podjęciu problematyki narodowowyzwoleńczej oraz postępującym antyurbanizmie i idealizacji wsi ("Cham" 1889). Największym dziełem Orzeszkowej jest powieść "Nad Niemnem" (1888), zawierająca epicki, wyidealizowany obraz ówczesnego życia ziemiańsko-szlacheckiego, w którym idea solidaryzmu szlachty i ludu łączy się z postulatem odrodzenia w młodym pokoleniu tradycji powstańczych 1863 r. Ponadto napisała opowiadania ("Widma" 1881) i powieści z krytyką narodnictwa i idei socjalistycznych oraz dekadentyzmu ("Dwa bieguny" 1893), nowele o powstaniu 1863 roku ("Gloria victis" 1910).
